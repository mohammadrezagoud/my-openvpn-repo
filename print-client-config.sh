#!/bin/bash
#
# print-client-config.sh
#
# محتوای فایل .ovpn را مستقیم و بدون هیچ رنگ یا دکوراسیونی در ترمینال چاپ می‌کند
# تا با کپی/پیست ساده به کلاینت OpenVPN منتقل شود.
#
# نکته: خط‌های راهنمای بالا و پایین با # شروع می‌شوند، یعنی کامنت هستند.
# اگر موقع کپی کردن این خط‌ها را هم با خودت ببری، مشکلی پیش نمی‌آید،
# چون OpenVPN خط‌هایی که با # شروع شوند را نادیده می‌گیرد.
#
# استفاده:
#   bash print-client-config.sh [نام کلاینت]
#   (اگر نام کلاینت داده نشود، پیش‌فرض "client" است)

set -euo pipefail

CLIENT_NAME="${1:-client}"
CLIENT_DIR="${HOME}/openvpn-clients"
OVPN_FILE="${CLIENT_DIR}/${CLIENT_NAME}.ovpn"

if [[ ! -f "$OVPN_FILE" ]]; then
    echo "فایل ${OVPN_FILE} پیدا نشد. اول codespace-openvpn.sh را اجرا کن یا نام کلاینت درست را بده." 1>&2
    exit 1
fi

echo "# ============ شروع کانفیگ (این خط را هم کپی کنی مشکلی نیست) ============"
cat "$OVPN_FILE"
echo "# ============ پایان کانفیگ (این خط را هم کپی کنی مشکلی نیست) ============"
